// Helper: get all questions tagged with a particular subject.
export function questionsBySubject(subject: Subject): QuizQuestion[] {
  const all: QuizQuestion[] = [];
  for (const day of Object.values(questionBank)) {
    for (const q of day) if (q.topic === subject) all.push(q);
  }
  return all;
}

export function allQuestions(): QuizQuestion[] {
  const out: QuizQuestion[] = [];
  for (const day of Object.values(questionBank)) out.push(...day);
  return out;
}
